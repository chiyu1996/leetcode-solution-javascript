/**
 * @param {number[]} nums
 * @return {number}
 */
var pivotIndex = function(nums) {
  const n = nums.length;
  if (!n) {
    return -1;
  }
  const sum = nums.reduce((prev, val) => prev + val);
  let pre = 0;

  for (let i = 0; i < n; i++) {
    if (pre === sum - pre - nums[i]) {
      return i
    }
    pre += nums[i];
  }
  return -1;
};
