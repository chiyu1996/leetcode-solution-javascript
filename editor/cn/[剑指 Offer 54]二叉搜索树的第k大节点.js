/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @param {number} k
 * @return {number}
 */
var kthLargest = function(root, k) {
  let ans;
  const dfs = (root) => {
    if (!root) return;
    dfs(root.right);
    if (k === 0) return;
    k--;
    if (k === 0) return ans = root.val;
    dfs(root.left);
  };
  dfs(root);
  return ans;
};
