/**
 * @param {string} s
 * @return {number}
 */
var lengthOfLastWord = function (s) {
    let ans = 0;
    let current = 0;
    for (let i = 0; i < s.length; i++) {
        if (s[i] === " ") {
            ans = current || ans;
            current = 0;
        } else {
            current++;
        }
    }
    if (current > 0) {
        ans = current;
    }
    return ans;
};
