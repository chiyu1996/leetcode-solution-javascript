/**
 * @param {number[]} nums
 * @return {string[]}
 */
var summaryRanges = function(nums) {
  let right;
  const result = [];
  const n = nums.length;
  for (let i = 0; i < n; i = right) {
    right = i + 1;
    while (right < n && nums[right] - nums[right - 1] === 1) {
      right++;
    }
    if (right === i + 1) {
      result.push(`${nums[i]}`);
    } else {
      result.push(`${nums[i]}->${nums[right - 1]}`)
    }
  }
  return result;
};
