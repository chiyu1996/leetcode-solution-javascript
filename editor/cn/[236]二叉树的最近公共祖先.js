/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @param {TreeNode} p
 * @param {TreeNode} q
 * @return {TreeNode}
 */
var lowestCommonAncestor = function (root, p, q) {
    let fa = {};
    let vis = {};
    const dfs = (root) => {
        if (root.left) {
            fa[root.left.val] = root;
            dfs(root.left);
        }
        if (root.right) {
            fa[root.right.val] = root;
            dfs(root.right);
        }
    };
    dfs(root);
    while (p) {
        vis[p.val] = true;
        p = fa[p.val];
    }
    while (q) {
        if (vis[q.val]) return q;
        q = fa[q.val];
    }
    return null;
};
