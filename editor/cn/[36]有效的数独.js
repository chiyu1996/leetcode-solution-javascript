/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function(board) {
  const col = new Array(9).fill(undefined).map(() => new Array(9).fill(0));
  const row = new Array(9).fill(undefined).map(() => new Array(9).fill(0));
  const box = new Array(9).fill(undefined).map(() => new Array(9).fill(0));
  for (let i = 0; i < 9; i++) {
    for (let j = 0; j < 9; j++) {
      if (board[i][j] == '.') {
        continue;
      }
      const val = board[i][j].codePointAt(0) - '1'.codePointAt(0);
      const box_index = (Math.floor(i / 3)) * 3 + Math.floor(j / 3);
      if (row[i][val] === 0 && col[j][val] === 0 && box[box_index][val] === 0) {
        row[i][val] = 1;
        col[j][val] = 1;
        box[box_index][val] = 1;
      } else {
        return false;
      }
    }
  }
  return true;
};
