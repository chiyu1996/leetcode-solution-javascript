/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number}
 */
var getMinimumDifference = function (root) {
    let pre;
    let ans = Infinity;
    let preVal = Infinity;
    while (root) {
        if (root.left) {
            pre = root.left;
            while (pre.right && pre.right !== root) {
                pre = pre.right;
            }
            if (!pre.right) {
                pre.right = root;
                root = root.left;

            } else {
                ans = Math.min(ans, Math.abs(root.val - preVal));
                preVal = root.val;
                pre.right = null;
                root = root.right;
            }
        } else {
            ans = Math.min(ans, Math.abs(root.val - preVal));
            preVal = root.val;
            root = root.right;
        }
    }
    return ans;
};
