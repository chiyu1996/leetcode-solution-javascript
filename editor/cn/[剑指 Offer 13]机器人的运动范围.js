/**
 * @param {number} m
 * @param {number} n
 * @param {number} k
 * @return {number}
 */
var movingCount = function (m, n, k) {
    if (!k) return 1;
    const dir = [[-1, 0], [0, -1], [1, 0], [0, 1]];
    let vis = new Array(m).fill(undefined).map(() => {
        return new Array(n).fill(0);
    });
    const getSum = (number) => {
        let ans = 0;
        while (number) {
            ans += number % 10;
            number = Math.floor(number / 10);
        }
        return ans;
    };
    let ans = 1;
    vis[0][0] = 1;
    let queue = [[0, 0]];
    while (queue.length) {
        let [col, row] = queue.shift();
        for (let i = 0; i < 4; i++) {
            let newCol = col + dir[i][0];
            let newRow = row + dir[i][1];
            if (newCol < 0 || newCol >= m || newRow < 0 || newRow >= n || vis[newCol][newRow] || getSum(newCol) + getSum(newRow) > k) continue;
            queue.push([newCol, newRow]);
            vis[newCol][newRow] = 1;
            ans++;
        }
    }
    return ans;
};
