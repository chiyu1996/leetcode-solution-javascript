/**
 * @param {number} n
 * @return {number}
 */
var minSteps = function(n) {
  let min = 0;
  let prime = 2;
  while (n > 1) {
    while (n % prime === 0) {
      min += prime;
      n /= prime;
    }
    prime++;
  }
  return min;
};
