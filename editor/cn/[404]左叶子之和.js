/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number}
 */
var sumOfLeftLeaves = function (root) {
    let ans = 0;
    const dfs = (root, isLeft) => {
        if (root === null) return;
        if (isLeft && root.left === null && root.right === null) {
            ans += root.val;
        }
        dfs(root.left, true);
        dfs(root.right, false);
    };
    dfs(root, false);
    return ans;
};
