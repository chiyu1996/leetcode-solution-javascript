/**
 * @param {number} x
 * @return {number}
 */
var reverse = function (x) {
    let rev = 0;
    let Max = (2 ** 31 - 1) / 10 | 0;
    let Min = -(2 ** 31) / 10 | 0;
    while (x !== 0) {
        let last = x % 10;
        x = x / 10 | 0;
        if (rev > Max || (rev === Max && last > 7)) return 0;
        if (rev < Min || (rev === Min && last < -8)) return 0;
        rev = rev * 10 + last;
    }
    return rev;
};
