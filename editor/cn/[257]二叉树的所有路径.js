/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {string[]}
 */
var binaryTreePaths = function (root) {
    let ans = [];
    const dfs = (root, path) => {

        if (root !== null) {
            path += root.val;
            if (!root.left && !root.right) {
                ans.push(path);
                return;
            } else {
                path += "->";
                dfs(root.left, path);
                dfs(root.right, path);
            }
        }
    };
    dfs(root, []);
    return ans;
};
